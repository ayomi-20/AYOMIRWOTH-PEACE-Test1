#create a program that calculates the are of a triangle
base = int(input("Enter the base: "))
height = int(input("Enter the height: "))
area = (1/2 * base * height)
print(f"The area of the triangle is {area}\n")
#alternatively
base = 10
height = 12
area = (1/2 * base * height)
print(f"The area of the triangle of base {base} and height {height} is {area}\n")
